#include <stdio.h>

int main()
{
    //定义一个字符变量a
    char  a;
    //给变量a赋值为'A'
    a = 'A';
    printf("变量a的值是 %c \n",a);

    a = 'B';//又把变量a的值改为B
    printf("变量a的值是 %c \n",a);


    //定义整型的变量
    int  b = 100;//赋值为100
    printf("变量b的值是 %d \n", b);

    b = 200; //改变变量的值为200
    printf("变量b的值是 %d \n", b);

    b = 300; //改变变量的值为300
    printf("变量b的值是 %d \n", b);

}
