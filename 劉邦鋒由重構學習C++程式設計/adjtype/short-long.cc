#include <iostream>
using namespace std;

int main(void)
{
  cout << "sizeof(short int) = " << sizeof(short int) << endl;
  cout << "sizeof(int) = " << sizeof(int) << endl;
  cout << "sizeof(long) = " << sizeof(long) << endl;
  cout << "sizeof(long int) = " << sizeof(long int) << endl;
  cout << "sizeof(long long int) = " << sizeof(long long int)
       << endl;
  return 0;
}
