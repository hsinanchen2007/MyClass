#include <stdio.h>

int main()
{
    /*
     * 1.在计算机中存储的所有数据都是补码
     * 2.在计算机中参与运算的都是补码
     * 3.如果计算的结果是一个正数, 那么直接将计算的结果转换为十进制就是我们想要的结果
     *   如果计算的结果是一个负数, 那么需要将计算的结果先转换为源码, 然后再转发为十进制才是我们想要的结果
     * 
     * 4.源码如何转换为反码
     * 符号位不变, 其它位按位取反
     * 5.反码如何转换为补码
     * 反码+1
     * 6.补码如何转换为反码
     * 补码-1
     * 7.反码如何转换为源码
     * 符号位不变, 其它位按位取反
     * 
     * 9 - 6 --> 9 + (-6)
     * 
     * 9的源码 0000 0000 0000 0000 0000 0000 0000 1001
     * 9的反码 0000 0000 0000 0000 0000 0000 0000 1001
     * 9的补码 0000 0000 0000 0000 0000 0000 0000 1001
     * 
     * -6的源码 1000 0000 0000 0000 0000 0000 0000 0110
     * -6的反码 1111 1111 1111 1111 1111 1111 1111 1001
     * -6的补码 1111 1111 1111 1111 1111 1111 1111 1010
     * 
     * 
     * 9的补码  0000 0000 0000 0000 0000 0000 0000 1001
     * -6的补码 1111 1111 1111 1111 1111 1111 1111 1010
     * ------------------------------------------------
     *        10000 0000 0000 0000 0000 0000 0000 0011
     *         0000 0000 0000 0000 0000 0000 0000 0011   --> 3
     * 
     * 4 - 6 --> 4 + (-6)
     * 4的源码 0000 0000 0000 0000 0000 0000 0000 0100
     * 4的反码 0000 0000 0000 0000 0000 0000 0000 0100
     * 4的补码 0000 0000 0000 0000 0000 0000 0000 0100
     * 
     * -6的源码 1000 0000 0000 0000 0000 0000 0000 0110
     * -6的反码 1111 1111 1111 1111 1111 1111 1111 1001
     * -6的补码 1111 1111 1111 1111 1111 1111 1111 1010
     * 
     * 4的补码  0000 0000 0000 0000 0000 0000 0000 0100
     * -6的补码 1111 1111 1111 1111 1111 1111 1111 1010
     * ------------------------------------------------
     *         1111 1111 1111 1111 1111 1111 1111 1110   补码
     * 由于参与运算的都是补码, 所以得到的结果也是补码, 并且得到的结果是一个负数, 所以需要先转换为源码
     *         1111 1111 1111 1111 1111 1111 1111 1101   反码
     *         1000 0000 0000 0000 0000 0000 0000 0010   源码 -2
     */ 
    return 0;
}
