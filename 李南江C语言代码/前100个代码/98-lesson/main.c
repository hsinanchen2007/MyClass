#include <stdio.h>
void printBinary(int value);
int main()
{
    // 需求: 要求输出一个整数的二进制的每一位
    // %i%d %o %x
    int num = 9;
    /*
     * 0000 0000 0000 0000 0000 0000 0000 1001
     *&0000 0000 0000 0000 0000 0000 0000 0001
     * -----------------------------------------
     * 0000 0000 0000 0000 0000 0000 0000 0001   --> 1
     *
     * 00000 0000 0000 0000 0000 0000 0000 100
     *&0000 0000 0000 0000 0000 0000 0000 0001
     * -----------------------------------------
     * 00000 0000 0000 0000 0000 0000 0000 000   --> 0
     *
     * 000000 0000 0000 0000 0000 0000 0000 10
     *&0000 0000 0000 0000 0000 0000 0000 0001
     * -----------------------------------------
     * 000000 0000 0000 0000 0000 0000 0000 00   --> 0
     *
     * 0000000 0000 0000 0000 0000 0000 0000 1
     *&0000 0000 0000 0000 0000 0000 0000 0001
     * -----------------------------------------
     * 0000000 0000 0000 0000 0000 0000 0000 1   --> 1
     */
    printBinary(num);
    return 0;
}
void printBinary(int value){
    // 1.定义一个变量记录右移的位数
    int offset = 31;
    // 2.通过循环来不断的右移
    while(offset >= 0){
        int res = (value >> offset) & 1;
        printf("%i", res);
        offset--;
    }
}
