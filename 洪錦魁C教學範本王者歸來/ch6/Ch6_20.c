/*   sin(x), cos(x) and tan(x)            */
#include <math.h>
#include <stdio.h>
#include <stdlib.h>
int main()
{
    double x;

    x = 3.1415926;
    printf("sin(x) --> %f  \n",sin(x));
    printf("cos(x) --> %f  \n",cos(x));
    printf("tan(x) --> %f  \n",tan(x));
    system("pause");
    return 0;    
}
