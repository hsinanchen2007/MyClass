/*   floor(x), ceil(x), fabs(x)           */
#include <math.h>
#include <stdio.h>
#include <stdlib.h>
int main()
{
    double x = 8.3;

    printf("floor(x) --> %f  \n",floor(x));
    printf("ceil(x)  --> %f  \n",ceil(x));
    printf("fabs(x)  --> %f  \n",fabs(x));
    system("pause");
    return 0;
}
