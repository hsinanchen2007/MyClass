/*   字元陣列結合                         */
#include <stdio.h>
#include <stdlib.h>
void str_con(char s1[],char s2[],char s3[], int n1, int n2)
{
    int j;
    
    for ( j = 0; j < n1; j++ ) 
        s3[j] = s1[j];       /* 結合第一個字串 */ 
    for ( j = n1; j < n1 + n2; j++ )
        s3[j] = s2[j - n1];  /* 結合第二個字串 */ 
}            
int main()
{
    char str1[5] = {'H','u','n','g',' '};
    char str2[9] = {'J','i','i','n','-','K','w','e','i'};
    char str3[14];
    int i;

    str_con(str1,str2,str3,5,9);
    for ( i = 0; i < 14; i++ ) /*列印結合後的字元 */ 
        printf("%c", str3[i]);
    printf("\n");    
    system("pause");
    return 0;    
}


















































































































































































































































































































































