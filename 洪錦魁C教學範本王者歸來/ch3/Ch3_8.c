/* 字元輸出的應用                         */
#include <stdio.h>
#include <stdlib.h>
int main()
{
    char ch = 'A';    /* 設定字元變數為 A */ 

    printf("ch = %c\n",ch);          /* 印變數 */ 
    printf("ASCII of ch = %d\n",ch); /* 印碼值 */ 
    system("pause");
    return 0;
}
