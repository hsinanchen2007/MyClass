/*
 * 範例1-1：最基本的C++程序
 * 這個程序展示了C++程序的最小結構
 * 只需要一個main函數就能構成有效的C++程序
 */
 
// 預處理指令：包含輸入輸出庫
#include <iostream>
 
/**
 * main函數是每個C++程序的入口點
 * 程序從這裡開始執行，也在這裡結束
 * 
 * int main() 表示：
 *   - int: 函數返回整數類型
 *   - main: 函數名稱（固定不變）
 *   - (): 參數列表（這裡為空）
 */
int main() {
    // std::cout 用於輸出文字到螢幕
    // << 是輸出運算符
    // std::endl 表示換行
    std::cout << "我的第一個C++程序" << std::endl;
    
    // return 0 表示程序正常結束
    // 0 是返回給操作系統的狀態碼
    return 0;
}
 