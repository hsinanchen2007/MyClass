/*
 * 範例1-2：多行輸出與註解
 * 展示如何輸出多行文字
 * 並演示不同類型的註解
 */
 
#include <iostream>  // 包含輸入輸出庫
 
// 單行註解：這是一個單行註解
// 單行註解以 // 開頭，直到行尾
 
/*
   多行註解（區塊註解）
   可以跨越多行
   以 /* 開始，以 *\/ 結束
*/
 
int main() {
    // 輸出第一行
    std::cout << "歡迎學習C++程式設計" << std::endl;
    
    // 輸出第二行（不加endl不會自動換行）
    std::cout << "這是";
    std::cout << "同一行文字" << std::endl;
    
    // 輸出第三行
    std::cout << "課程大綱：" << std::endl;
    
    // 使用多個 << 運算符連接輸出
    std::cout << "1. 基本語法" << std::endl
              << "2. 變數與數據類型" << std::endl
              << "3. 流程控制" << std::endl;
    
    return 0;  // 程序結束，返回0表示成功
}
 